            <div property="schema:text" class="clearfix text-formatted field field--name-body field--type-text-with-summary field--label-hidden field__item"><h1 class="text-align-center">International Political Economy Society</h1>

<p> </p>

<table align="center" border="0" cellpadding="5" cellspacing="5" class="table borderless table-responsive"><tbody><tr><td style="width: 220px;border:none;"><img alt="logo" data-entity-type="file" data-entity-uuid="c785ee6f-d641-4d8a-86a4-d154fd029b83" height="186" src="sites/default/files/inline-images/logo2_0.png" width="178" class="align-center" /><p> </p>
			</td>
			<td style="width: 15px;border:none;"> </td>
			<td style="width: 627px;border:none;">
			<p>The International Political Economy Society (IPES) provides an annual forum for scholars of IPE to present their best new work in progress to an informed and critical scholarly audience. The annual conference is centered on a small number (approximately 75 each year) of carefully screened and selected papers.</p>

			<p>The <a href="node/45"><strong>2016 conference</strong></a> will be held November 11 and 12. It will be co-hosted by Duke University and the University of North Carolina and held in Durham. The program is available <a href="node/56"><strong>here.</strong></a></p>

			<p>Read IPES's response to the NC HB-2 legislation <strong><a href="node/51">here.</a></strong></p>
