            <div property="schema:text" class="clearfix text-formatted field field--name-body field--type-text-with-summary field--label-hidden field__item"><h1 class="text-align-center"><strong>About </strong>IPES<strong><img alt="ipeslogo" data-entity-type="file" data-entity-uuid="4056939e-8083-4440-9016-84176b74529a" height="198" src="../sites/default/files/inline-images/ipeslogo.png" width="172" class="align-right" /></strong></h1>

<p>The International Political Economy Society (IPES) provides an annual forum for scholars of IPE to present their best new work in progress to an informed and critical scholarly audience. The annual conference is centered on a small number (approximately 75 each year) of carefully screened and selected papers.</p>

<p>IPES is an open-membership society. There are no membership dues or restrictions on participation. We welcome all scholars of international political economy to join the society by registering via the links at left. This will add you to our mailing list and keep you informed of future IPES events. If your contact information has changed since you last registered, please follow the update link.</p>

