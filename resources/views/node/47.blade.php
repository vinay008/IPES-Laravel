<div class="xh" id="2014" style="display:none;"><br />
The ninth annual meeting of the International Political Economy Society was held November 14-15, 2014 at Georgetown University. The conference program is available <a href="program2014">here</a>.</div>

<div class="xh" id="2013" style="display:none;"><br />
The eighth annual meeting of the International Political Economy Society was held October 25-26, 2013 at the Claremont Graduate School. The program is available <a href="program2013">here</a>.</div>

<div class="xh" id="2012" style="display:none;"><br />
The seventh annual meeting of the International Political Economy Society was held November 9-10, 2012 at the University of Virginia.</div>

<div class="xh" id="2011" style="display:none;"><br />
The sixth annual meeting of the International Political Economy Society was held November 11-12, 2011 at the University of Wisconsin, Madison.</div>

<div class="xh" id="2010" style="display:none;"><br />
The fifth annual meeting of the International Political Economy Society was held November 12-13, 2010 at the Weatherhead Center for International Affairs, Harvard University, Cambridge, MA.</div>

<div class="xh" id="2009" style="display:none;"><br />
The fourth annual meeting of the International Political Economy Society was held November 13-14, 2009 at Texas A&amp;M University in College Station, Texas. The meeting was hosted by the Scowcroft Institute for International Affairs and the George H. W. Bush School of Government and Public Service at Texas A&amp;M, which generously agreed to underwrite some costs for the conference.</div>

<div class="xh" id="2008" style="display:none;"><br />
The 2008 annual conference of IPES was held November 14-15 at the University of Pennsylvania, Philadelphia, PA. The meeting was hosted by the Christopher H. Browne Center for International Politics, which has generously agreed to underwrite the costs of the conference. We are deeply grateful to the staff of the Browne Center for their assistance in organizing this conference.</div>

<div class="xh" id="2007" style="display:none;"><br />
The second annual meeting of IPES was held on November 9 and 10 at Stanford University. The meeting was hosted by the Division of International, Comparative and Area Studies (ICA), directed by Judith Goldstein. We are deeply grateful to the staff of ICA for their assistance in organizing this conference.</div>

<div class="xh" id="2006" style="display:none;"><br />
The first annual meeting of IPES was held on November 17 and 18 at Princeton University. The meeting was hosted by the Princeton Center for Globalization and Governance, directed by Helen Milner. We are deeply grateful to the staff of the Center for their assistance in organizing this conference.</div>
