<h2 class="text-align-center"><strong>Virtual IPES</strong></h2>

<p><strong>Virtual IPES</strong> aims to further integrate and socialize the IPE/CPE community by serving as an outlet for the many papers that the annual IPES meeting is unable to accommodate, improving the chances that the papers discussed are published in reputable outlets.</p>

<p>Papers accepted to a Virtual IPES session will be circulated to four discussants each of whom will be asked to prepare comments. An online meeting using Google Hangout will follow where the author may give a brief overview of the paper and then receives feedback from each discussant. The schedule of workshops will be posted online so others interested in the topics may join in and provide feedback.</p>

