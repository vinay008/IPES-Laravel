            <div class="clearfix text-formatted field field--name-body field--type-text-with-summary field--label-hidden field__item"><h2 class="text-align-center"><strong>Current conference</strong></h2>

<p>The 2016 meeting will be held November 11 and 12. It will be co-hosted by Duke University and the University of North Carolina and held in Durham. Professors Eddy Malesky (Dept. of Political Science, Duke University), Sarah Bermeo (Sanford School of Public Policy, Duke University), and Layna Mosley (Department of Political Science, UNC Chapel Hill) are our local hosts. Logistical information about the conference is available <a href="https://sites.duke.edu/ipes/">here</a>.</p>

<p>Breakfast and lunch on Friday and Saturday of the conference will be provided as well as a dinner for all participants on Friday evening. Please plan to attend these meals as they are an important opportunity for networking within the conference. Presenters are also strongly encouraged to attend the entire meeting.</p>

<p>The Aloft Hotel in downtown Durham, at the American Tobacco campus, will serve as the IPES conference hotel. We will provide transportation from the hotel to the Sanford School at Duke University, where all conference sessions will take place. Friday night’s dinner will be held at the Durham Bulls Athletic Park, which is a short walk from the Aloft. For those opting to stay for Saturday night’s dinner in Chapel Hill, we will provide transportation from the Aloft. More information about the hotel and the surrounding area: <a href="http://www.aloftdurhamdowntown.com/">http://www.aloftdurhamdowntown.com/</a></p>

<p>The Aloft is offering a rate of $145/night to IPES conference participants. This rate is available only through October 10, 2016, using these links: <a href="https://urldefense.proofpoint.com/v2/url?u=https-3A__www.starwoodmeeting.com_Book_IPESConference&amp;d=CwMGaQ&amp;c=imBPVzF25OnBgGmVOlcsiEgHoG1i6YHLR0Sj_gZ4adc&amp;r=zg91ZGojr6d68nUG8ycfpTJhwoiX0th3ysQr8ICG04w&amp;m=VWGz9QecReCPVwaGuQsei6yLdRqNWtmUsGL7QmBFvfk&amp;s=qtHorDyKbNbOuWgDPXzOyL_rBDwE3tGq6mnS9fRJCns&amp;e=">IPES Conference Duke University School of Political Science</a>, or <a href="https://www.starwoodmeeting.com/Book/IPESConference">https://www.starwoodmeeting.com/Book/IPESConference</a>.</p>

<p>For booking by phone rather than on line, contact Kay Hess at 919-402-5656, extension 5670, and reference IPES Conference, Duke University.</p>
