            <div property="schema:text" class="clearfix text-formatted field field--name-body field--type-text-with-summary field--label-hidden field__item"><h1 class="text-align-center">Conferences</h1>

<h3><strong>2016 conference<img alt="conf1" data-entity-type="file" data-entity-uuid="0a8588ba-6b10-44d2-baa7-9c00a4762c6b" height="183" src="../sites/default/files/inline-images/b2_1.jpg" width="326" class="align-right" /></strong></h3>

<p>The <a href="45">2016 conference</a> will be held November 11 and 12. It will be co-hosted by Duke University and the University of North Carolina and held in Durham.</p>

<p> </p>

<h3><strong>Format</strong></h3>

<p>Each paper is assigned to its own time slot. The author is given 12 minutes to present his or her paper, leaving approximately 15 minutes for questions and discussion from the audience. There is no discussant. After 27 minutes, the session is ended, leaving three minutes to switch rooms. Because all three rooms must run on exactly the same schedule, all time limits will be strictly enforced by session chairs. Authors should carefully plan their presentations to make sure they are no longer than 12 minutes. Multiple presenters on the same paper are discouraged as they almost always exceed the time limit.</p>

<p> </p>

<h3><strong>Conference Expenses and Funding</strong></h3>

<p>The IPES does not have any permanent institutional support. We will be charging a registration fee of $50 for faculty and $25 for graduate students attending the conference. All participants must <strong>pre-register</strong> for conferences. Instructions and links will be available on the <a href="https://ncgg.princeton.edu/IPES/drupal/index.php">IPES homepage</a> as the conference approaches.</p>

<p>All participants are expected to pay their own travel and hotel expenses. We understand that travel funds are limited for everyone. We hope that the high quality of the meeting and discussions will make this a worthwhile expense.</p>

<p> </p>

