            <div property="schema:text" class="clearfix text-formatted field field--name-body field--type-text-with-summary field--label-hidden field__item"><h1 class="text-align-center"><strong>IPES and NC HB2</strong></h1>

<p> </p>

<h3><strong>The letter below details the view of the IPES Steering Committee regarding its November 2016 conference. To summarize:</strong></h3>

<p>• Members of the IPES Steering Committee are unanimous in their strong opposition to the State of North Carolina’s HB-2 legislation.<br />
• Despite this opposition, we intend to hold the meeting in Durham, NC, on the campus of Duke University (a private institution, not bound to enforce HB-2, and on record as strongly opposed to the legislation).<br />
• At the same time, we respect that some IPES members may want to express opposition to state of North Carolina’s policy by boycotting the meeting.</p>

<p><strong>Dear members of the IPES community:</strong></p>

<p>We write to provide additional information on our consideration of the siting of our conference in Durham, NC, co-organized by Duke University and the University of North Carolina at Chapel Hill. As most of you are aware, the HB-2 legislation passed by the state of North Carolina in March 2016 has been widely criticized, not only for Part I (the “bathroom bill” component), but also for Parts II and III, which roll back local ordinances protecting LGBT and other individuals from discrimination. The bill also limits the capacity of local governments to, among other things, increase minimum wages.</p>

<p>This legislation has been roundly criticized locally, nationally and internationally. A lawsuit seeking to overturn the legislation was filed in U.S. District Court on March 28, and North Carolina’s own Attorney General has refused to defend the law. A range of companies have signed a statement asking North Carolina to repeal the law; last week, Paypal announced that it was changing its plans to site in Charlotte, NC; Deutsche Bank made a similar announcement regarding a planned expansion to Cary, NC this week; and Bruce Springsteen recently cancelled a concert planned for Greensboro, NC.</p>

<p>In light of the legislation and the related efforts to effect a repeal via boycotts, a few of you have asked whether we will move forward with the co-hosting of the conference by Duke and UNC. The steering committee and the local organizers certainly are aware of this legislation, and we are gravely concerned about its implications for residents of North Carolina. We also want to be confident that conference attendees feel comfortable during their visit to the IPES meeting. Our main purpose as an association entering its second decade is to continue to build a diverse intellectual community, and we very much want everyone to feel comfortable as we advance that work.</p>

<p>At the same time, we recognize that many individuals, entities and local governments in North Carolina are firmly opposed to the legislation and, in many cases, working to advocate for its repeal. The scholarly communities at Duke and UNC are strongly committed to non-discrimination and equal treatment, and these communities are very keen to welcome all members of IPES to Durham. Over the years, host institutions’ graduate students have noted the significant positive impact that IPES has had on their professional and intellectual development. It would be unfortunate to penalize graduate students at Duke and UNC for the legislation passed by members of a state legislature that do not represent them.</p>

<p>Indeed, those of you who are familiar with North Carolina will not be surprised that the local governments of Carrboro, Chapel Hill and Durham, North Carolina (the three municipal entities that overlap with Duke and UNC) each have unanimously passed resolutions calling for the bill’s repeal. On March 24, Duke University issued a statement noting that “we deplore any effort to deny any person the protection of the law because of sexual orientation or gender identity.” As a private institution, Duke is not bound to enforce the provisions of HB2.</p>

<p>We also are aware that the President of the UNC system has stated that, as a public institution, UNC is required to enforce the legislation on its 17 campuses. At the same time, UNC Chapel Hill's chancellor has reminded the campus community that all of UNC's anti-discrimination policies remain in place. Given the former, we will not be holding any conference-related activities on UNC’s campus.</p>

<p>Additionally, the economic impact of an IPES relocation is likely to fall not on those who support this legislation, but on a local community that is very much opposed to it. Durham, and especially its downtown area (where the conference hotel is located), is a diverse, tolerant and vibrant city. Downtown has undergone a renaissance during the last few years, and it will benefit materially from our presence. Similarly, Chapel Hill/Carrboro (which will host our optional Saturday dinner) have been statewide leaders on issues of equality. These areas, despite their opposition to statewide legislation, would lose lodging, meal and entertainment expenditures were we to relocate.</p>

<p>There likely are a range of opinions among the IPES community regarding how to proceed, and we appreciate that not all of you will share our sense of the best way forward. We have deliberated on this issue, and we agree that there is unlikely to be a solution that will please everyone.</p>

<p>We hope that you will make plans to attend IPES and that you will continue to support us in our efforts to build and deepen our scholarly community. We are looking forward to receiving your proposals for IPES 2016, which are due on April 15.</p>

<p>Sincerely,</p>

<h3 class="text-align-justify"><strong>The IPES Steering Committee</strong></h3>

